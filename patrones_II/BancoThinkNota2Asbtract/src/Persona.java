public class Persona{
    
  private String NombeCompleto;
  private int rut;
  private int fono;
  private String dirA;
  private String dirB;

    public String getNombeCompleto() {
        return NombeCompleto;
    }

    public void setNombeCompleto(String NombeCompleto) {
        this.NombeCompleto = NombeCompleto;
    }

    public int getRut() {
        return rut;
    }

    public void setRut(int rut) {
        this.rut = rut;
    }

    public int getFono() {
        return fono;
    }

    public void setFono(int fono) {
        this.fono = fono;
    }

    public String getDirA() {
        return dirA;
    }

    public void setDirA(String dirA) {
        this.dirA = dirA;
    }

    public String getDirB() {
        return dirB;
    }

    public void setDirB(String dirB) {
        this.dirB = dirB;
    }
  
}
